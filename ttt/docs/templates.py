# Category page
# ---------------------------------------------------------------------------

def render_category(category: str, entries) -> str:
    """Render a category listing page."""
    title = category.replace("scripts", " Scripts").replace("events", " Events").title()

    # Build header columns based on category
    extra_cols = _category_columns(category)
    header = "<th>Name</th><th>Script</th>"
    for col_name, _ in extra_cols:
        header += f"<th>{_html_escape(col_name)}</th>"
    header += "<th>Description</th>"

    rows = ""
    for entry in entries:
        link = f'<a href="detail_{_safe_filename(entry.category, entry.name)}.html">{_html_escape(entry.name)}</a>'
        row = f"<td>{link}</td><td>{_html_escape(entry.script)}</td>"
        for _, attr_key in extra_cols:
            val = entry.attributes.get(attr_key, "")
            row += f"<td>{_html_escape(str(val))}</td>"
        row += f"<td>{_html_escape(entry.description[:100])}</td>"
        rows += f"<tr>{row}</tr>\n"

    body = f"""
  <h1>{_html_escape(title)}</h1>
  <p class="subtitle">{len(entries)} entries</p>

  <table>
    <thead><tr>{header}</tr></thead>
    <tbody>{rows}</tbody>
  </table>
"""
    return _page(title, body, active=title.split()[0])


def _category_columns(category: str):
    """Return extra columns for a category: list of (label, attr_key)."""
    if category == "actions":
        return [("Item ID", "itemid"), ("Action ID", "actionid")]
    elif category == "movements":
        return [("Type", "type"), ("Item ID", "itemid"), ("Action ID", "actionid")]
    elif category == "talkactions":
        return [("Words", "words"), ("Access", "access")]
    elif category == "creaturescripts":
        return [("Event", "type"), ("Name", "name")]
    elif category == "globalevents":
        return [("Type", "type"), ("Interval", "interval")]
    elif category == "npcs":
        return [("Keywords", "keywords"), ("Health", "health")]
    elif category == "spells":
        return [("Mana", "mana"), ("Level", "lvl"), ("Words", "words")]
    return []


# ---------------------------------------------------------------------------
# Detail page
# ---------------------------------------------------------------------------

def render_detail(entry) -> str:
    """Render a script detail page."""
    title = entry.name

    # Attributes list
    attrs_html = ""
    if entry.attributes:
        attrs_html = '<div class="meta">'
        for k, v in entry.attributes.items():
            attrs_html += f'<span><strong>{_html_escape(k)}:</strong> {_html_escape(str(v))}</span>'
        attrs_html += "</div>"

    # Code block
    code_html = ""
    if entry.lua_content:
        code_html = f"<h3>Source Code</h3>\n<pre><code>{_html_escape(entry.lua_content)}</code></pre>"

    body = f"""
  <h1>{_badge(entry.category)} {_html_escape(entry.name)}</h1>
  <p class="subtitle">
    Script: {_html_escape(entry.script)} &mdash;
    Category: {_html_escape(entry.category)}
  </p>

  <div class="detail-card">
    {attrs_html}
    <p>{_html_escape(entry.description)}</p>
  </div>

  {code_html}
"""
    cat_title = entry.category.replace("scripts", " Scripts").replace("events", " Events").title()
    return _page(title, body, active=cat_title.split()[0])


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _safe_filename(category: str, name: str) -> str:
    """Create a filesystem-safe filename from category + name."""
    safe = re.sub(r'[^\w\-]', '_', f"{category}_{name}")
    return safe[:80]
