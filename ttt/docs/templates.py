# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _html_escape(text: str) -> str:
    return (text
            .replace("&", "&amp;")
            .replace("<", "&lt;")
            .replace(">", "&gt;")
            .replace('"', "&quot;"))


def _nav(active: str = "") -> str:
    links = [
        ("index.html", "Overview"),
        ("actions.html", "Actions"),
        ("movements.html", "Movements"),
        ("talkactions.html", "TalkActions"),
        ("creaturescripts.html", "CreatureScripts"),
        ("globalevents.html", "GlobalEvents"),
        ("npcs.html", "NPCs"),
        ("spells.html", "Spells"),
    ]
    parts = ['<nav><span class="brand">TTT Docs</span>']
    for href, label in links:
        cls = ' class="active"' if label.lower() == active.lower() else ""
        parts.append(f'<a href="{href}"{cls}>{label}</a>')
    parts.append("</nav>")
    return "".join(parts)


def _page(title: str, body: str, active: str = "") -> str:
    return f"""<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
<title>{_html_escape(title)} - TTT Server Docs</title>
<style>{_CSS}</style>
</head>
<body>
{_nav(active)}
<div class="container">
{body}
</div>
<footer>Generated by TTT &mdash; OTServer Developer Toolkit</footer>
</body>
</html>"""


def _badge(category: str) -> str:
    badge_map = {
        "actions": "action",
        "movements": "movement",
        "talkactions": "talkaction",
        "creaturescripts": "creature",
        "globalevents": "global",
        "npcs": "npc",
        "spells": "spell",
    }
    cls = badge_map.get(category, "action")
    label = category.replace("scripts", "").replace("events", "").capitalize()
    return f'<span class="badge badge-{cls}">{_html_escape(label)}</span>'


# ---------------------------------------------------------------------------
# Index page
# ---------------------------------------------------------------------------

def render_index(report) -> str:
    """Render the main index page."""
    cats = report.categories
    stats_html = ""
    for cat_name, entries in cats.items():
        if not entries:
            continue
        label = cat_name.replace("scripts", " Scripts").replace("events", " Events").title()
        stats_html += f"""
    <div class="stat-box">
      <div class="stat-value">{len(entries)}</div>
      <div class="stat-label"><a href="{cat_name}.html">{_html_escape(label)}</a></div>
    </div>"""

    # Recent entries table (first 20 across all categories)
    all_entries = []
    for cat_name, entries in cats.items():
        for e in entries:
            all_entries.append(e)
    rows = ""
    for entry in all_entries[:20]:
        script_link = f'<a href="detail_{_safe_filename(entry.category, entry.name)}.html">{_html_escape(entry.name)}</a>'
        rows += f"""
      <tr>
        <td>{_badge(entry.category)}</td>
        <td>{script_link}</td>
        <td>{_html_escape(entry.script)}</td>
        <td>{_html_escape(entry.description[:80])}</td>
      </tr>"""

    body = f"""
  <h1>Server Documentation</h1>
  <p class="subtitle">Directory: {_html_escape(report.directory)} &mdash; {report.total_entries} entries documented</p>

  <div class="stats">
    <div class="stat-box">
      <div class="stat-value">{report.total_entries}</div>
      <div class="stat-label">Total Entries</div>
    </div>
    {stats_html}
  </div>

  <h2>All Entries</h2>
  <table>
    <thead>
      <tr><th>Type</th><th>Name</th><th>Script</th><th>Description</th></tr>
    </thead>
    <tbody>
      {rows}
    </tbody>
  </table>
"""
    return _page("Overview", body, active="Overview")


